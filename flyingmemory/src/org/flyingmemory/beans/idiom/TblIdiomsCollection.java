package org.flyingmemory.beans.idiom;

public class TblIdiomsCollection {

	private String idiomId;
	private String idiom;
	private String idiomInitials;
	private String idiomDesc;
	private String idiomStatus;
	private String instDate;
	private String instTime;
	/**
	 * @return the idiomId
	 */
	public String getIdiomId() {
		return idiomId;
	}
	/**
	 * @param idiomId the idiomId to set
	 */
	public void setIdiomId(String idiomId) {
		this.idiomId = idiomId;
	}
	/**
	 * @return the idiom
	 */
	public String getIdiom() {
		return idiom;
	}
	/**
	 * @param idiom the idiom to set
	 */
	public void setIdiom(String idiom) {
		this.idiom = idiom;
	}
	/**
	 * @return the idiomInitials
	 */
	public String getIdiomInitials() {
		return idiomInitials;
	}
	/**
	 * @param idiomInitials the idiomInitials to set
	 */
	public void setIdiomInitials(String idiomInitials) {
		this.idiomInitials = idiomInitials;
	}
	/**
	 * @return the idiomDesc
	 */
	public String getIdiomDesc() {
		return idiomDesc;
	}
	/**
	 * @param idiomDesc the idiomDesc to set
	 */
	public void setIdiomDesc(String idiomDesc) {
		this.idiomDesc = idiomDesc;
	}
	/**
	 * @return the idiomStatus
	 */
	public String getIdiomStatus() {
		return idiomStatus;
	}
	/**
	 * @param idiomStatus the idiomStatus to set
	 */
	public void setIdiomStatus(String idiomStatus) {
		this.idiomStatus = idiomStatus;
	}
	/**
	 * @return the instDate
	 */
	public String getInstDate() {
		return instDate;
	}
	/**
	 * @param instDate the instDate to set
	 */
	public void setInstDate(String instDate) {
		this.instDate = instDate;
	}
	/**
	 * @return the instTime
	 */
	public String getInstTime() {
		return instTime;
	}
	/**
	 * @param instTime the instTime to set
	 */
	public void setInstTime(String instTime) {
		this.instTime = instTime;
	}
	
}
